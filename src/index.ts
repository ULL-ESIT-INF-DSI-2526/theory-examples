/**
 * Function that adds two numbers
 * @param firstNumber - The first operand of the addition
 * @param secondNumber - The second operand of the addition
 * @returns The addition between `firstOperand` and `secondOperand`
 * 
 * ```typescript
 * add(1, 7) // Returns number 8
 * add(3.5, 5.1) // Returns number 8.6
 * ```
 */

export function add(firstNumber: number, secondNumber: number): number {
  return firstNumber + secondNumber;
}
